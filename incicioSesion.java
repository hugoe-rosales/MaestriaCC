    private void btnIrAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIrAppActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText().trim().toUpperCase();
        String contrase人 = txtContrase人.getText().trim();
        boolean credencialesValidas = false;
        if (validarControles())
            if (listaUsuarios.size() > 0){
                for(Usuario usr: listaUsuarios){
                    if (usr.getUsuario().trim().toUpperCase().contentEquals(usuario) && usr.getContrase人().contentEquals(contrase人)){
                        credencialesValidas = true;
                        lanzaFrameAgenda();
                    }
                }
                if (credencialesValidas == false) { JOptionPane.showMessageDialog(null, "CREDENCIALES NO V첿IDAS...\n\nINGRESE UN NOMBRE DE USUARIO Y CONTRASE헤 CORRECTOS!"); }
            } else 
                JOptionPane.showMessageDialog(null, "ACTUALMENTE NO HAY USUARIOS REGISTRADOS...\n\nREGISTRE UN NOMBRE DE USUARIO Y CONTRASE헤 PARA INGRESAR A LA AGENDA!");
    }//GEN-LAST:event_btnIrAppActionPerformed

    private void lanzaFrameAgenda(){
        JFrame frame = new Proyecto_Final();
        this.setVisible(false);
        frame.show();
    }
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText().trim().toUpperCase();
        String contrase人 = txtContrase人.getText().trim();
        boolean usrRegistrado = false;
        if (usuario.length() > 0 && contrase人.length() > 0)
            for(Usuario usr: listaUsuarios)
                if (usr.getUsuario().contentEquals(usuario))
                    usrRegistrado = true;
            
            if (usrRegistrado == false){
                if (guardarUsuario())
                    lanzaFrameAgenda();
                else 
                    JOptionPane.showMessageDialog(null, "INGRESE LOS DATOS DE USUARIO Y CONTRASE헤!");
            } else 
                JOptionPane.showMessageDialog(null, "EL NOMBRE DE USUARIO YA HA SIDO REGISTRADO!");
    }//GEN-LAST:event_btnRegistrarActionPerformed


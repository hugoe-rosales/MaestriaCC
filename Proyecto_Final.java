    private boolean validarControles(){
        String emailFormato ="@.";  
        String telformato = "1234567890";

        if (txtNombre.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "INGRESE UN  [NOMBRE]  DE CONTACTO.");
            txtNombre.requestFocusInWindow();
            return false;
        } else if (txtApellidos.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "INGRESE LOS  [APELLIDOS]  DEL CONTACTO.");
            txtApellidos.requestFocusInWindow();
            return false;
        } else if (txtTelefono.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "INGRESE UN  [TELEFONO]  VÁLIDO DEL CONTACTO.");
            txtTelefono.requestFocusInWindow();
            return false;
        } else if (txtTelefono.getText().trim().length() > 10){
            JOptionPane.showMessageDialog(null, "LA LONGITUD DEL NÚMERO DE  [TELEFONO]  DEBE SER 10 DÍGITOS.");
            txtTelefono.requestFocusInWindow();
            return false;
        } else if (txtCorreo.getText().trim().length() == 0 || txtCorreo.getText().trim().contains(" ") || 
                  !txtCorreo.getText().trim().contains("@") || !txtCorreo.getText().trim().contains(".")){
            JOptionPane.showMessageDialog(null, "INGRESE UN   [CORREO]  VALIDO DEL CONTACTO.");
            txtCorreo.requestFocusInWindow();
            return false;
        }
        return true;
    }
    

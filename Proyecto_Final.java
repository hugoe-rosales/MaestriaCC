    private void cargarDatosTxt(){
        String linea = "";
        String dfModel = "";
        dfListModel = new DefaultListModel();

        try{
            FileReader archivoDatos = new FileReader(archivoSalida);
            BufferedReader buffer = new BufferedReader(archivoDatos);
            while ((linea = buffer.readLine()) != null) {
                String[] datosContacto = linea.split(",");
                if (linea.trim().length() > 0 && datosContacto.length > 0){
                    Agenda a = new Agenda();
                    dfModel = datosContacto[0] + "," + datosContacto[2] + "," + datosContacto[1];
                    a.setId(datosContacto[0]); a.setNombre(datosContacto[1]);   a.setApellidos(datosContacto[2]);
                                               a.setTelefono(datosContacto[3]); a.setCorreo(datosContacto[4].toLowerCase());
                    listaContactos.add(a);
                    dfListModel.addElement(dfModel);
                }
            }
            lstContactos.setModel(dfListModel);
            buffer.close();
            archivoDatos.close();
        } catch(IOException e){
            e.printStackTrace();
        }
    }
    
    private void guardarContactos(){
        int id = 1;
        try{
            if (listaContactos.size() >= 0){
                File archivo = new File(archivoSalida);
                archivo.delete();
                FileWriter archSalida = new FileWriter(archivoSalida, true);
                BufferedWriter bufSalida = new BufferedWriter(archSalida);
                for(Agenda agenda: listaContactos){
                    bufSalida.write(String.valueOf(id) + "," + agenda.getNombre() + "," + agenda.getApellidos() + "," + agenda.getTelefono() + "," + agenda.getCorreo() + "\n");
                    id += 1;
                }
                bufSalida.close(); archSalida.close();
                inicializar();
                JOptionPane.showMessageDialog(null, "INFORMACIÓN ACTUALIZADA EXITOSAMENTE!");     
            }
            else
                JOptionPane.showMessageDialog(null, "IMPOSIBLE GUARDAR...\n\nLA LISTA DE CONTACTOS ESTA VACÍA!");                                    
        } catch(IOException e){
            e.printStackTrace();
        }
    }
    
    private void inicializar(){
        File directorio = new File(rutaArchivo);
        if (!directorio.exists()) 
            if (directorio.mkdirs()) 
                System.out.println("Directorio creado");
            else 
                System.out.println("Error al crear directorio");
        txtNombre.setText(""); txtApellidos.setText(""); txtTelefono.setText(""); txtCorreo.setText("");
        listaContactos.clear();  cargarDatosTxt();
    }
    
    private boolean controlContactos(boolean llenarControles, boolean eliminarContacto, boolean guardaContacto){
        int index = lstContactos.getSelectedIndex();
        String elementoSel = lstContactos.getSelectedValue();
        if (llenarControles){
            String[] contacto = lstContactos.getSelectedValue().split(",");
            for(Agenda agenda: listaContactos)
                if (agenda.getNombre().contains(contacto[2]) &&  agenda.getApellidos().contains(contacto[1])){
                    txtNombre.setText(agenda.getNombre()); txtApellidos.setText(agenda.getApellidos());
                    txtTelefono.setText(agenda.getTelefono()); txtCorreo.setText(agenda.getCorreo().toLowerCase());
                }
        } else if(eliminarContacto){
            if (index != -1){
                String[] contacto = lstContactos.getSelectedValue().split(",");
                for(Agenda contact: listaContactos){
                    if (contact.getNombre().trim().contains(contacto[2].trim()) && contact.getApellidos().trim().contains(contacto[1].trim())){
                        listaContactos.remove(index);
                        guardarContactos();
                        return true;
                    }
                }
            }
        } else if (guardaContacto) {
            String nombre = txtNombre.getText().trim().toUpperCase();
            String apellidos = txtApellidos.getText().trim().toUpperCase();
            String telefono = txtTelefono.getText().trim().toUpperCase();
            String correo = txtCorreo.getText().trim().toLowerCase();
            if (validarControles()){
                if (index == -1){
                    Agenda a = new Agenda();
                    a.setId("*"); a.setNombre(nombre);     a.setApellidos(apellidos);
                                  a.setTelefono(telefono); a.setCorreo(correo);
                    listaContactos.add(a);
                } else {
                    String[] contacto = elementoSel.split(",");
                    for(Agenda contact: listaContactos){
                        if(Integer.parseInt(contact.getId()) == Integer.parseInt(contacto[0])){
                            contact.setNombre(nombre);     contact.setApellidos(apellidos);
                            contact.setTelefono(telefono); contact.setCorreo(correo);
                        }            
                    }
                }
                guardarContactos();
            }
        }
        //lstContactos.clearSelection();
        return true;
    }            
    

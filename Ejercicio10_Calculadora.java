    private void agregaTexto(char valor, boolean bGuardaNumero){
        if (!lblValores.getText().trim().contains("ERROR")){
            if (resultado > 0 && bGuardaNumero == false) { txtOperaciones.setText(""); }
            if (resultado > 0 && bGuardaNumero ) { lblValores.setText(String.valueOf(resultado)); }
            if (resultado > 0) { resultado = 0; }
            String cadenaAritmetica = txtOperaciones.getText().trim();
            String valorCasilla = "";
            if (bGuardaNumero && numero1 == 0)
            {
                numero1 = Double.valueOf(cadenaAritmetica);
                operador = valor;         
            }
            else if (bGuardaNumero && numero1 > 0)
                numero2 = Double.valueOf(cadenaAritmetica);
            valorCasilla = (bGuardaNumero) ? "" : cadenaAritmetica + valor;
            txtOperaciones.setText(valorCasilla);   
            lblValores.setText(" " + lblValores.getText() +  valor);
        }
    }
    

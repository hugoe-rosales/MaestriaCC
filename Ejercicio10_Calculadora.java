    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
        if (lblValores.getText().trim().indexOf('=') == -1){
            agregaTexto(' ', true);
            resultado = 0;
            switch(operador){
                case 'x':
                    resultado = numero1 * numero2;
                    break;
                case '÷':
                    resultado = numero1 / numero2;
                    break;
                case '+':
                    resultado = numero1 + numero2;
                    break;
                case '-':
                    resultado = numero1 - numero2;
                    break;
            }
            String cadenaResultado = (operador == '÷' && numero2 == 0) ? "ERROR DE DIV ÷ CERO" : String.valueOf(resultado); 
            if (operador == '÷' && numero2 == 0)
                lblValores.setText(lblValores.getText() + "= " + "ERROR DE DIV ÷ CERO");
            else
                lblValores.setText(lblValores.getText() + "= " + String.valueOf(resultado));
            txtOperaciones.setText(cadenaResultado);
            inicializaVariables();
        }
    }//GEN-LAST:event_btnIgualActionPerformed

